package com.momentum.impl.modules.render.chams;

/**
 * Chams render mode
 *
 * @author linus
 * @since 03/11/2023
 */
public enum ChamsMode {

    /**
     * Fills the model
     */
    FILL,

    /**
     * Outlines the model
     */
    WIREFRAME,

    /**
     * Renders both modes
     */
    BOTH
}
